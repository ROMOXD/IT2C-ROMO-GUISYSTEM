    private void delete_buttonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_delete_buttonMouseClicked
 
    int selectedRow = urTable.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(null, "Please select a room to delete.");
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this room?", "Confirm Delete", JOptionPane.YES_NO_OPTION);
    if (confirm != JOptionPane.YES_OPTION) {
        return;
    }

    String roomId = urTable.getValueAt(selectedRow, 0).toString();
    String deleteQuery = "DELETE FROM tbl_room WHERE room_id = '" + roomId + "'";

    dbConnector dbc = new dbConnector();
    if (dbc.insertData(deleteQuery)) {
        JOptionPane.showMessageDialog(null, "Room deleted successfully!");
        displayData();

    Session sess = Session.getInstance();
    String type = sess.getType();

    if (type.equals("Admin")) {
        AdminPage hmp = new AdminPage();
        hmp.setVisible(true);
    } else if (type.equals("User")) {
        UserPage usp = new UserPage();
        usp.setVisible(true);
    } else {
        JOptionPane.showMessageDialog(this, "Unknown account type: " + type);
        return;
    }

    Window window = SwingUtilities.getWindowAncestor(this);
    if (window != null) {
        window.dispose();
    }

    } else {
        JOptionPane.showMessageDialog(null, "Failed to delete room. Please try again.");
    }
    }//GEN-LAST:event_delete_buttonMouseClicked

