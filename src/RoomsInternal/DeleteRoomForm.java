     private void updateRoomPrice() {
        String selectedRoomType = (String) r_type.getSelectedItem();
        String priceText = "";

        switch (selectedRoomType) {
            case "Standard Room":
                priceText = "500 Pesos";
                break;
            case "Deluxe Room":
                priceText = "750 Pesos";
                break;
            case "Suite Room":
                priceText = "1000 Pesos";
                break;
            default:
                priceText = "Unknown";
        }

        rp.setText(priceText);
    }

