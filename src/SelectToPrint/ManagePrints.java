    private void print_buttonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_print_buttonMouseClicked
    int userRow = usersTable.getSelectedRow();
    int guestRow = guestsTable.getSelectedRow();

    dbConnector dbc = new dbConnector();

    if (userRow != -1) {
        int selectedUserId = Integer.parseInt(usersTable.getValueAt(userRow, 0).toString());

        try {
            ResultSet rs = dbc.getData("SELECT * FROM tbl_user WHERE u_id = '" + selectedUserId + "'");
            if (rs.next()) {

                UserPrinting up = new UserPrinting();

                up.id.setText("" + rs.getInt("u_id"));
                up.fn.setText(rs.getString("u_fname"));
                up.ln.setText(rs.getString("u_lname"));
                up.at.setText(rs.getString("u_type"));
                up.em.setText(rs.getString("u_email"));
                up.cn.setText(rs.getString("u_cnum"));
                up.un.setText(rs.getString("u_usern"));
                up.gd.setText(rs.getString("u_gen"));
                up.ag.setText("" + rs.getInt("u_age"));
                up.st.setText(rs.getString("u_status"));

                String imagePath = rs.getString("u_image");
                if (imagePath != null && !imagePath.isEmpty()) {
                    File file = new File(imagePath);
                    if (file.exists()) {
                        AddUserForm auf = new AddUserForm(); 
                        up.profile.setIcon(auf.ResizeImage(imagePath, null, up.profile));
                        up.profile.setText("");
                    } else {
                        up.profile.setText("NO PROFILE");
                        up.profile.setIcon(null);
                    }
                } else {
                    up.profile.setText("NO PROFILE");
                    up.profile.setIcon(null);
                }

                up.setVisible(true);
                this.dispose();
                
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error fetching user data.");
        }

    } else if (guestRow != -1) {
        int selectedBookingId = Integer.parseInt(guestsTable.getValueAt(guestRow, 0).toString());

        try {
            ResultSet rs = dbc.getData("SELECT * FROM tbl_bookings WHERE b_id = '" + selectedBookingId + "'");
            if (rs.next()) {

                GuestPrinting gp = new GuestPrinting(); 

                gp.fn.setText(rs.getString("g_fname"));
                gp.ln.setText(rs.getString("g_lname"));
                gp.rid.setText("" + rs.getInt("rm_id"));
                gp.rt.setText(rs.getString("rm_type"));
                gp.ag.setText("" + rs.getInt("g_age"));
                gp.em.setText(rs.getString("g_email"));
                gp.cin.setText(rs.getString("b_cin"));
                gp.cout.setText(rs.getString("b_cout"));
                gp.cs.setText("" + rs.getDouble("b_cash"));
                gp.cng.setText("" + rs.getDouble("b_change"));

                String imagePath = rs.getString("g_image");
                if (imagePath != null && !imagePath.isEmpty()) {
                    File file = new File(imagePath);
                    if (file.exists()) {
                        AddUserForm auf = new AddUserForm();
                        gp.profile.setIcon(auf.ResizeImage(imagePath, null, gp.profile));
                        gp.profile.setText("");
                    } else {
                        gp.profile.setText("NO PROFILE");
                        gp.profile.setIcon(null);
                    }
                } else {
                    gp.profile.setText("NO PROFILE");
                    gp.profile.setIcon(null);
                }

                gp.setVisible(true);
                this.dispose();
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error fetching booking data.");
        }
    } else {
        JOptionPane.showMessageDialog(this, "Please select a user or guest row to print.");
    }
    }//GEN-LAST:event_print_buttonMouseClicked

