    private void jLabel13MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel13MouseClicked
    Session sess = Session.getInstance();
    String type = sess.getType();

    if (type.equals("Admin")) {
        AdminPage hmp = new AdminPage();
        hmp.setVisible(true);
    } else if (type.equals("User")) {
        UserPage usp = new UserPage();
        usp.setVisible(true);
    } else {
        JOptionPane.showMessageDialog(this, "Unknown account type: " + type);
        return;
    }

    this.dispose();
    }//GEN-LAST:event_jLabel13MouseClicked

    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
       
         Session sess = Session.getInstance();

   if (sess.isHasRecoveryQuestions()) {
    l_recover.setEnabled(false);       
    l_recover.setForeground(Color.GRAY);  
    canClickRecovery = false;            
    } else {
    l_recover.setEnabled(true);
    l_recover.setForeground(Color.BLUE);
    canClickRecovery = true;
}
    }//GEN-LAST:event_formWindowActivated

