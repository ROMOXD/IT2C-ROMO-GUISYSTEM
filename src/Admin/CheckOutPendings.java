    private void cout_buttonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cout_buttonMouseClicked
        int selectedRow = c_table.getSelectedRow();

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a booking to check out.");
        return;
    }

    try {
        dbConnector dbc = new dbConnector();

        String bookingId = c_table.getValueAt(selectedRow, 0).toString();
        String roomId = c_table.getValueAt(selectedRow, 1).toString();

        String updateBooking = "UPDATE tbl_bookings SET b_status = 'Checked-Out' WHERE b_id = '" + bookingId + "'";
        dbc.updateData(updateBooking);

        String updateRoom = "UPDATE tbl_room SET r_status = 'Vacant' WHERE room_id = '" + roomId + "'";
        dbc.updateData(updateRoom);

        JOptionPane.showMessageDialog(this, "Guest checked out successfully!");
        
    displayCheckoutData();

    Session sess = Session.getInstance();
    String type = sess.getType();

    if (type.equals("Admin")) {
        AdminPage hmp = new AdminPage();
        hmp.setVisible(true);
    } else if (type.equals("User")) {
        UserPage usp = new UserPage();
        usp.setVisible(true);
    } else {
        JOptionPane.showMessageDialog(this, "Unknown account type: " + type);
        return;
    }

    this.dispose();

    } catch (Exception ex) {
        System.out.println("Check-out Error: " + ex.getMessage());
        JOptionPane.showMessageDialog(this, "Error during check-out. Please try again.");
    }
    }//GEN-LAST:event_cout_buttonMouseClicked

