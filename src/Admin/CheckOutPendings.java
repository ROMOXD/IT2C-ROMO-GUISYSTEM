    public void displayCheckoutData() {
    try {
        dbConnector dbc = new dbConnector();
        String today = new SimpleDateFormat("yyyy-MM-dd").format(new Date());

        String sql = "SELECT b_id, rm_id, g_fname, g_lname, b_cin, b_cout, b_status " +
                     "FROM tbl_bookings " +
                     "WHERE b_status = 'Approved' AND b_cout <= '" + today + "'";

        ResultSet rs = dbc.getData(sql);
        c_table.setModel(DbUtils.resultSetToTableModel(rs));
        rs.close();
    } catch (SQLException ex) {
        System.out.println("Checkout Display Error: " + ex.getMessage());
    }
}
    
