    public static boolean recoverAccount(String username, String answer1, String answer2, String answer3) {
        dbConnector dbc = new dbConnector();
        Session sess = Session.getInstance();
        
        try {
            String query = "SELECT * FROM tbl_recovery WHERE user_id = (SELECT u_id FROM tbl_user WHERE u_usern = '" + username + "')";
            ResultSet resultSet = dbc.getData(query);

            if (resultSet.next()) {
                
                String storedAns1 = resultSet.getString("f_ans");
                String storedAns2 = resultSet.getString("s_ans");
                String storedAns3 = resultSet.getString("t_ans");

                if (storedAns1.equals(answer1) && storedAns2.equals(answer2) && storedAns3.equals(answer3)) {
                    int userId = resultSet.getInt("user_id");
                    sess.setUid(userId);
                    System.out.println("Account recovery successful.");
                    return true;
                } else {
                    System.out.println("Incorrect recovery answers.");
                    return false;
                }
            } else {
                System.out.println("No recovery answers set for this user.");
                return false;
            }
        } catch (SQLException ex) {
            System.out.println(""+ex);
            return false;
        }
    }

